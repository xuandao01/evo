            hợp Khách hàng rời máy tính và chưa  đăng xuất trên website, hệ thống sẽ tự động đăng xuất sau một khoảng thời gian.

            III. THỜI GIAN LƯU TRỮ THÔNG TIN

            EVO sẽ lưu trữ thông tin thu thập từ Khách hàng từ thời điểm Khách hàng khai báo thông tin đến khi tài khoản của Khách hàng ngừng hoạt động.
