            Ban quản trị website EVO có thể chia sẻ, tiết lộ thông tin cá nhân của Khách hàng khi tin rằng việc làm đó là cần thiết để bảo vệ các quyền lợi 
            của Khách hàng, đảm bảo sự an toàn giao dịch cho Khách hàng và theo yêu cầu của bên thứ 3 có thẩm quyền.

