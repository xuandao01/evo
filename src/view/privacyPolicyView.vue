            Cam kết bảo mật tuyệt đối mọi thông tin giao dịch trực tuyến của Khách hàng bao gồm các thông tin về lịch sử giao dịch; thông tin về chứng từ số hóa 
            tại khu vực dữ liệu trung tâm an toàn;

